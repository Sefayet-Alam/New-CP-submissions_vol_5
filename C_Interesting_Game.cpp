/*
If we start from i we need to divide it to a1,a2,a3... so that a1+1=a2,a2+1=a3... and a1+a2+a3..=i (current stone in the pile)
Let's calculate grundy nos..we can go from 0 to nowhere...so g[0]=0...similarly, g[1]=g[2]=0...now for g[3], we can go to 1 and 2..
NOTE that it's not like we can go to 1 OR 2...We must go both...As we can go to 0 from this (g[1] xor g[2]=0), g[3]=1...g[4]=0..
now g[5]=0 but why? coz we can go to 
g[2] xor g[3] which is 1(winning state), therefore g[5]=0 (losing state)..
implementation approach: dp..
now suppose, a value can be summed as a1+a2+a3 and again b1+b2 ...then 
g[i] will be mex of these xors (i.e :mex of  g[a1]^g[a2]^g[a3] and g[b1]^b2 as OR logic)
*/
ll dp[N]; //calculates grundy value overall
ll ans[N];

ll func(ll n){
    if(n==0) return dp[0]=0;
    if(dp[n]!=-1) return dp[n];
    set<ll>stt;
    for(ll i=1;i<min(n,500LL);i++){
        ll z=(i*(i+1))/2;
        if(z>n) break;
        //lets say the series is y+y+1+y+2,...+y+k=n
        //then n=(k+1)*y+sum(1 to k+1)
        //n=(k+1)*y+z or n-z=(k+1)*y
        if((n-z)%(i+1)==0){
            ll xr=0;
            //starting of consecutive nos: y=(n-z)/(i+1)
            for(ll j=0;j<=i;j++){
                xr^=func((n-z)/(i+1)+j);
            }
            //found that this position is winning position
            if(xr==0 && ans[n]==-1){
                ans[n]=i+1;//minm no of piles
            }
            stt.insert(xr);
        }
    }
    // deb2(n,stt);
    ll mex=0;
    while (stt.find(mex)!=stt.end())
    {
        mex++;
    }
    
    return dp[n]=mex;
}
